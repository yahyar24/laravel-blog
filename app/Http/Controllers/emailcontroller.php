     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'email_sub' => 'required|max:255',
            
            
        ]);



         email::create([
            'email_sub' => $request->email_sub,
            
        ]);



        Session::flash('success' ,' تم الاشتراك بنجاح.');

       return redirect()->back();
    }

